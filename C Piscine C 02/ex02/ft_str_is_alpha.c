#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	i;
	int	numero;

	numero = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'a') || (str[i] > 'z'))
		{
			if ((str[i] < 'A') || (str[i] > 'Z'))
			{
				numero = 0;
			}
		}
		i++;
	}
	return (numero);
}
