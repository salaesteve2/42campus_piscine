#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}

int main()
{
    char cadena[6] = "hOa5a";
	char *resultado;

    resultado = ft_strlowcase(cadena);
    printf("%s",resultado);
}