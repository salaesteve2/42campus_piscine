#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	j = 0;
	if (nb <= 1)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
